Breakpoint:
Een breakpoint is een markeringspunt in de broncode van een programma waar de uitvoering stopt,
waardoor je de mogelijkheid hebt om de toestand van het programma te inspecteren en variabelen te
controleren voordat je verdergaat met debuggen.

Step into:
"Step into" is een debug-opdracht die je gebruikt om de uitvoering van het programma naar de volgende regel code te brengen.
Als de huidige regel een functieaanroep bevat,
zal de debugger naar binnen stappen en de uitvoering binnen die functie voortzetten.

Step out:
"Step out" is een debug-opdracht die wordt gebruikt om uit de huidige functie
te stappen en terug te keren naar de functie waaruit de huidige functie werd aangeroepen.
Hierdoor kun je snel door een reeks functieaanroepen gaan zonder in detail de uitvoering van elke functie te hoeven volgen.

Add to watches:
"Add to watches" is een functie in een debugger waarmee je variabelen kunt toevoegen aan de "watchlist" of het "watches-venster".
Door variabelen aan de watchlist toe te voegen,
kun je hun waarden tijdens het debuggen in de gaten houden en controleren hoe ze veranderen naarmate het programma wordt uitgevoerd.
Dit is handig om specifieke variabelen te volgen en te controleren of ze de verwachte waarden hebben.
